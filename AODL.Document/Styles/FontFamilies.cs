		internal static string HtmlFont(string ooFont)
		{
			try
			{
				string[] strArray = new string[] { "0", "1", "2", "3" };
				for (int i = 0; i < (int)strArray.Length; i++)
				{
					ooFont = ooFont.Replace(strArray[i], "");
				}
			}
			catch (Exception exception)
			{
			}
			return ooFont;
		}

		internal static string PtToPx(string point)
		{
			string str;
			try
			{
				int num = Convert.ToInt32(point.Replace("pt", ""));
				if (num < 12)
				{
					num++;
				}
				str = string.Concat(num.ToString(), "pt");
				return str;
			}
			catch (Exception exception)
			{
			}
			str = point;
			return str;
		}
